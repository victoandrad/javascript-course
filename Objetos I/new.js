// Operador New
// Usado para criar uma nova instância de um objeto

// Quando não usado o new, é retornado o return da função
const data1 = Date()
console.log(data1)
console.log(typeof data1)

// Quando usado o new, é retornado o objeto contendo todas as propriedades e métodos
const data2 = new Date()
console.log(data2)
console.log(typeof data2)