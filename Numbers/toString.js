// toString()
// Método usado para transformar um número em uma String
// Pode receber como parâmetro os valores 2, 10 e 16 para fazer conversão de bases numéricas

let numero = 15
console.log(numero.toString(2))
console.log(typeof numero.toString(2))

console.log(numero.toString(10))
console.log(typeof numero.toString(10))

console.log(numero.toString(16))
console.log(typeof numero.toString(16))